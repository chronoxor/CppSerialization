template<typename CharT, typename Traits>
friend std::basic_ostream<CharT, Traits> & operator << (
    std::basic_ostream<CharT, Traits> &builder, const Account &_writer)
{
    Account writer(
        _writer.m_buffer,
        _writer.m_offset,
        _writer.m_bufferLength,
        _writer.m_actingBlockLength,
        _writer.m_actingVersion);

    builder << '{';
    builder << R"("Name": "Account", )";
    builder << R"("sbeTemplateId": )";
    builder << writer.sbeTemplateId();
    builder << ", ";

    builder << R"("id": )";
    builder << +writer.id();

    builder << ", ";
    builder << R"("wallet": )";
    builder << writer.wallet();

    builder << ", ";
    {
        bool atLeastOne = false;
        builder << R"("orders": [)";
        writer.orders().forEach(
            [&](Orders &orders)
            {
                if (atLeastOne)
                {
                    builder << ", ";
                }
                atLeastOne = true;
                builder << orders;
            });
        builder << ']';
    }

    builder << ", ";
    builder << R"("name": )";
    builder << '"' <<
        writer.getNameAsJsonEscapedString().c_str() << '"';

    builder << '}';

    return builder;
}

